# ---------------------------------------------------------------------------
# Cloudinary (image hosting). Configure with either a single CLOUDINARY_URL
# (cloudinary://API_KEY:API_SECRET@CLOUD_NAME) or the three separate vars.
# ---------------------------------------------------------------------------
import cloudinary  # noqa: E402

CLOUDINARY_URL = os.getenv("CLOUDINARY_URL")
CLOUDINARY_CLOUD_NAME = os.getenv("CLOUDINARY_CLOUD_NAME")
CLOUDINARY_API_KEY = os.getenv("CLOUDINARY_API_KEY")
CLOUDINARY_API_SECRET = os.getenv("CLOUDINARY_API_SECRET")
CLOUDINARY_UPLOAD_FOLDER = os.getenv("CLOUDINARY_UPLOAD_FOLDER", "impal-food")

if CLOUDINARY_URL:
    # The SDK auto-reads the CLOUDINARY_URL env var on first use.
    cloudinary.config(secure=True)
elif CLOUDINARY_CLOUD_NAME and CLOUDINARY_API_KEY and CLOUDINARY_API_SECRET:
    cloudinary.config(
        cloud_name=CLOUDINARY_CLOUD_NAME,
        api_key=CLOUDINARY_API_KEY,
        api_secret=CLOUDINARY_API_SECRET,
        secure=True,
    )

CLOUDINARY_ENABLED = bool(CLOUDINARY_URL or (CLOUDINARY_CLOUD_NAME and CLOUDINARY_API_KEY and CLOUDINARY_API_SECRET))
